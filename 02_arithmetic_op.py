num1 = int(input("Enter 1st Number : "))
num2 = int(input("Enter 2nd Number : "))

add = num1 + num2
sub = num1 - num2
mul = num1 * num2
div = num1 / num2

print(f"{num1} + {num2} = {add}")
print(f"{num1} - {num2} = {sub}")
print(f"{num1} * {num2} = {mul}")
print(f"{num1} / {num2} = {div}")